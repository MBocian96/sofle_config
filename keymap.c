void tap_based_on(bool clockwise, uint16_t code_up, uint16_t code_down){
    uint16_t code = code_up;
    if (clockwise){
        code = code_down;
    }
    tap_code(code);
}

void basic_encoder_settings(uint8_t index, bool clockwise){
    switch(index){
        case 0:
            tap_based_on(clockwise, KC_VOLU, KC_VOLD);
            break;
        case 1:
            tap_based_on(clockwise, KC_WH_U, KC_WH_D);
            break;
    }
}

void lightroom_encoder_settings(uint8_t index, bool clockwise){
    switch(index){
        case 0:
            tap_based_on(clockwise, KC_MINS, KC_EQL);
            break;
        case 1:
            tap_based_on(clockwise, KC_VOLU, KC_VOLD);
            break;
    }
}

bool encoder_update_user(uint8_t index, bool clockwise) {
    switch (biton32(layer_state)){
        case _LRT:
            lightroom_encoder_settings(index, clockwise);
            break;
        default:
            basic_encoder_settings(index, clockwise);
            break;
        }
    return true;
}

